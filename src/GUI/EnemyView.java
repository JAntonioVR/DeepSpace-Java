    public void setEnemyStarShip(EnemyToUI enemy){
        LootView lootView=new LootView();
        DamageView damageView=new DamageView();
        
        name.setText(enemy.getName());
        ammoPower.setText(Float.toString(enemy.getAmmoPower()));
        shieldPower.setText(Float.toString(enemy.getShieldPower()));
        LootToUI loot=enemy.getLoot();
        lootView.setLoot(loot);
        DamageToUI damage=enemy.getDamage();
        damageView.setDamage(damage);
        
        lootPanel.removeAll();
        lootPanel.add(lootView);
        damagePanel.removeAll();
        damagePanel.add(damageView);
        repaint();
        revalidate();
    }
    

